unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int src_size;
	unsigned int i;

	src_size = 0;
	i = 0;
	while (src[src_size] != '\0')
		src_size++;
	if (size != 0)
	{
		while (src[i] != '\0' && i < (size - 1))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (src_size);
}
